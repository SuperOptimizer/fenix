TEST(dct3d_decode_golden) {
    std::array<u8, io::dct3d::kN3> out{};
    REQUIRE(io::dct3d::decode<u8>(std::span<const u8>(kDct3dBlob, sizeof kDct3dBlob), std::span<u8>(out)));
    int max_ref = 0, max_orig = 0;
    for (usize i = 0; i < out.size(); ++i) {
        max_ref = std::max(max_ref, std::abs(static_cast<int>(out[i]) - static_cast<int>(kDct3dRef[i])));
        max_orig = std::max(max_orig, std::abs(static_cast<int>(out[i]) - static_cast<int>(kDct3dOrig[i])));
    }
    CHECK(max_ref <= 1);    // agrees with the reference decoder
    CHECK(max_orig <= 64);  // honors the encode-time tau bound
    // malformed input: wrong magic / wrong dtype / truncation must fail, not fill
    std::array<u8, io::dct3d::kN3> junk{};
    std::vector<u8> bad(kDct3dBlob, kDct3dBlob + sizeof kDct3dBlob);
    bad[0] = 0x00;
    CHECK(!io::dct3d::decode<u8>(bad, std::span<u8>(junk)));
    std::vector<u8> wrongdt(kDct3dBlob, kDct3dBlob + sizeof kDct3dBlob);
    wrongdt[1] = 2;  // u16 tag on a u8 blob
    CHECK(!io::dct3d::decode<u8>(wrongdt, std::span<u8>(junk)));
    CHECK(!io::dct3d::decode<u8>(std::span<const u8>(kDct3dBlob, 1), std::span<u8>(junk)));
}
